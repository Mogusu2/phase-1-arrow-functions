// Function expression called divide
const divide = function() {
    return 2000 / 100;
};

// Arrow function called square
const square = (num) => {
    return num * num;
};

// Arrow function called add
const add = (a, b) => {
    return a + b;
};

// Example Usage:
console.log(divide()); // 20
console.log(square(89)); // 16
console.log(add(41, 96)); // 8
